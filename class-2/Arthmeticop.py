#1 Arthmetic operators

# Addition

print(8 + 3) 
 # Output: 8

# Subtraction

print(8 - 3) 
 # Output: 5

# Multiplication

print(8 * 3)  
# Output: 24

# Floor division (integer division)

print(8 // 3)  
# Output: 2

# Exponentiation

print(2 ** 3) 
 # Output: 8

# Modulo (remainder)

print(8 % 3) 
 # Output: 2