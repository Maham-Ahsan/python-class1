# Logical Operators in Python

# AND operator (and) - Returns True if both operands are True
# Example:
x = 8
print (x > 6 and x < 11 )

# OR operator (or) - Returns True if either of the operands is True

# Example:

x = 10
y = 15

print (x > 5 or y < 5)

# NOT operator (not) - Returns True if the operand is False

# Example:

x = False
print (not x)
